def get_greeting_text():
    """Get time-based greeting in current language."""
    import datetime
    hour = datetime.datetime.now().hour
    if 5 <= hour < 12:
        return lang("good_morning")
    elif 12 <= hour < 15:
        return lang("good_afternoon")
    elif 15 <= hour < 18:
        return lang("good_evening")
    else:
        return lang("good_night")


