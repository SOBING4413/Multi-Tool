def lang(key, **kwargs):
    """Get translated string for the current language.
    
    Args:
        key: Translation key from TRANSLATIONS dict
        **kwargs: Optional format arguments for string interpolation
    
    Returns:
        Translated string, or English fallback, or the key itself if not found.
    """
    global CURRENT_LANGUAGE
    entry = TRANSLATIONS.get(key)
    if entry is None:
        return key
    text = entry.get(CURRENT_LANGUAGE, entry.get("en", key))
    if kwargs:
        try:
            text = text.format(**kwargs)
        except (KeyError, IndexError):
            pass
    return text


def set_language(code):
    """Set the current language.
    
    Args:
        code: Language code (e.g., 'en', 'id', 'es', etc.)
    
    Returns:
        True if language was set successfully, False otherwise.
    """
    global CURRENT_LANGUAGE
    if code in LANGUAGES:
        CURRENT_LANGUAGE = code
        return True
    return False


def get_current_language():
    """Get the current language code."""
    return CURRENT_LANGUAGE


def get_current_language_name():
    """Get the display name of the current language."""
    info = LANGUAGES.get(CURRENT_LANGUAGE, {})
    return f"{info.get('flag', '')} {info.get('native', CURRENT_LANGUAGE)}"


def get_available_languages():
    """Get list of available languages with metadata.
    
    Returns:
        dict: Language code -> {name, flag, native}
    """
    return LANGUAGES.copy()


